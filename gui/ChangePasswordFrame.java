    private boolean validateInput(){
        userName = txtfUserName.getText();
        char[] currentPswd = pswdfCurrentPassword.getPassword();
        char[] newPswd = pswdfNewPassword.getPassword();
        if(userName.isEmpty() || currentPswd.length==0 ||
                currentPswd.length==0 || pswdfConfirmPassword.getPassword().length==0)
            return false;
        
        currentPassword = String.valueOf(currentPswd);
        newPassword = String.valueOf(newPswd);
        
        return true;
    }
    private void clearAll()
    {
        pswdfCurrentPassword.setText("");
        pswdfNewPassword.setText("");
        pswdfConfirmPassword.setText("");
    }
