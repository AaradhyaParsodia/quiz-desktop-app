    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        boolean checkInput = validateInput();
        if(!checkInput)
        {
            JOptionPane.showMessageDialog(null, "Please Fill All Details First","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        User user = new User();
        user.setUserId(userName);
        user.setPassword(currentPassword);
        user.setUserType("Student");
        
        try
        {
            boolean validateUser = QuizAppDao.validateUser(user);
            
            if(!validateUser)
            {
                JOptionPane.showMessageDialog(null, "!! Incorrect Current Password !!","Error",JOptionPane.ERROR_MESSAGE);
                clearAll();
                return;
            }
            
            char[] pswd = pswdfConfirmPassword.getPassword();
            String confirmPswd = String.valueOf(pswd);
            
            if(!newPassword.equals(confirmPswd))
            {
                JOptionPane.showMessageDialog(null, "Both New And Confirm Password Does Not Match\nBoth The Password must contain the same password","Password Not Match",JOptionPane.ERROR_MESSAGE);
                clearAll();
                return;
            }
            
            
            int ans = JOptionPane.showConfirmDialog(null, "Sure You want to Update/Change Password ?","Confirm ?",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
            if(ans==JOptionPane.NO_OPTION)
            {
                return;
            }
            
            user.setPassword(newPassword);
            
            boolean result = QuizAppDao.changeStudentPassword(user);
            
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Password Has Change !","Chnaged Password",JOptionPane.INFORMATION_MESSAGE);
                
                clearAll();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Password Is Not Chnaged.\nPlease Try Again Later.","Error Occured",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "!DB Error!","Change Student Password Frame Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnDoTaskActionPerformed

