    private void cbbSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbSubjectActionPerformed
        
        boolean result = validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null, "Please Select a subject First!","Error",JOptionPane.ERROR_MESSAGE);
            cbbSubject.setSelectedIndex(0);
            return;
        }
//        System.out.println("SubjectName: "+subjectName);
        try
        {
            ArrayList<String> examList = ExamDao.getExamIdBySubject(subjectName);
            if(examList.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No exam is set for "+subjectName,"Exam Not Found",JOptionPane.ERROR_MESSAGE);
                return;
            }
            cbbExamId.removeAllItems();
            for(String examId : examList)
            {
                cbbExamId.addItem(examId);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "!DB Error!","Edit Paper Frame Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_cbbSubjectActionPerformed
    
    private boolean validateInput()
    {
        int selectedIndex = cbbSubject.getSelectedIndex();
        if(selectedIndex==0)
           return false;
        subjectName = cbbSubject.getSelectedItem().toString().trim();
        return true;
    }
