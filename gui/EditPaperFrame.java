    private void btnEditQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionActionPerformed
     
        boolean result = validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null, "Please Select a subject First!","Error",JOptionPane.ERROR_MESSAGE);
            cbbSubject.setSelectedIndex(0);
            return;
        }
        String subject = cbbSubject.getSelectedItem().toString().trim();
        String examId = cbbExamId.getSelectedItem().toString().trim();
        int ans;
        ans = JOptionPane.showConfirmDialog(null, "Sure You want to Edit exam "+examId+" and Subject of "+subject+" ?","Confirm ?",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try
            {
                int totalQuestion = ExamDao.getQuestionCountByExamId(examId);
//                System.out.println("ExamId: "+examId+"\nTotal Questions: "+totalQuestion);
                Exam editExam = new Exam(examId,subject,totalQuestion);
                EditQuestionsFrame editQuestions = new EditQuestionsFrame(editExam);
                editQuestions.setVisible(true);
                this.dispose();
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "!DB Error!","Edit Paper Frame Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_btnEditQuestionActionPerformed

